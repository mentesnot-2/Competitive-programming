from typing import List
from queue import Queue
class Solution:
    #Function to return Breadth First Traversal of given graph.
    def bfsOfGraph(self, V: int, adj: List[List[int]]) -> List[int]:
        lst=[]
        visited=set()
        que=[0]
        while que:
            val=que.pop(0)
            if val not in visited:
                for i in adj[val]:
                    if i not in visited:
                        que.append(i)
                visited.add(val)
                lst.append(val)
        return lst
                    
